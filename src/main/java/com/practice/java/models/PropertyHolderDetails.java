public class PropertyHolderDetails {

	private String propertyHolderName;
	private String propertyHoldererPhNo;
	private String holderSC;
	private String propertyManagerName;
	private String propertyManagerPhNo;
	public String getPropertyHolderName() {
		return propertyHolderName;
	}
	public void setPropertyHolderName(String propertyHolderName) {
		this.propertyHolderName = propertyHolderName;
	}
	public String getPropertyHoldererPhNo() {
		return propertyHoldererPhNo;
	}
	public void setPropertyHoldererPhNo(String propertyHoldererPhNo) {
		this.propertyHoldererPhNo = propertyHoldererPhNo;
	}
	public String getHolderSC() {
		return holderSC;
	}
	public void setHolderSC(String holderSC) {
		this.holderSC = holderSC;
	}
	public String getPropertyManagerName() {
		return propertyManagerName;
	}
	public void setPropertyManagerName(String propertyManagerName) {
		this.propertyManagerName = propertyManagerName;
	}
	public String getPropertyManagerPhNo() {
		return propertyManagerPhNo;
	}
	public void setPropertyManagerPhNo(String propertyManagerPhNo) {
		this.propertyManagerPhNo = propertyManagerPhNo;
	}
	
	
}
